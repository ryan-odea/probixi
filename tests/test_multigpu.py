def test_resolve_devices_explicit_list():
    devs = resolve_devices(["cpu", "cpu"])
    assert devs == [torch.device("cpu"), torch.device("cpu")]


def test_resolve_devices_int_gives_cuda_indices():
    devs = resolve_devices(3)
    assert devs == [
        torch.device("cuda:0"),
        torch.device("cuda:1"),
        torch.device("cuda:2"),
    ]


def test_resolve_devices_rejects_empty():
    with pytest.raises(ValueError):
        resolve_devices([])
    with pytest.raises(ValueError):
        resolve_devices(0)


