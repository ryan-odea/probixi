def test_crystal_block_has_cell_and_reflections(tmp_path, geometry_dict, cell):
    out = tmp_path / "indexed.stream"
    with DataOffloader(out, geometry=geometry_dict, cell=cell) as off:
        off.write(_make_index_result(cell))

    lines = out.read_text().splitlines()
    crystal = _section(lines, "--- Begin crystal", "--- End crystal")

    # cell parameters: writer emits nm (A / 10) -> 62.23 A -> 6.22300 nm, etc.
    cell_line = next(line for line in crystal if line.startswith("Cell parameters"))
    parsed = cell_line.split()
    a_nm, b_nm, c_nm = float(parsed[2]), float(parsed[3]), float(parsed[4])
    assert parsed[5] == "nm,"
    assert a_nm == pytest.approx(cell.a * 0.1, abs=1e-4)
    assert b_nm == pytest.approx(cell.b * 0.1, abs=1e-4)
    assert c_nm == pytest.approx(cell.c * 0.1, abs=1e-4)
    al, be, ga = float(parsed[6]), float(parsed[7]), float(parsed[8])
    assert parsed[9] == "deg"
    assert al == pytest.approx(math.degrees(cell.alpha), abs=1e-2)
    assert be == pytest.approx(math.degrees(cell.beta), abs=1e-2)
    assert ga == pytest.approx(math.degrees(cell.gamma), abs=1e-2)

    # reciprocal basis lines present and parse to the A columns (A^-1 -> nm^-1)
    astar = next(line for line in crystal if line.startswith("astar = "))
    bstar = next(line for line in crystal if line.startswith("bstar = "))
    cstar = next(line for line in crystal if line.startswith("cstar = "))
    a_vals = [float(t) for t in astar.split()[2:5]]
    assert astar.endswith("nm^-1")
    assert bstar.endswith("nm^-1") and cstar.endswith("nm^-1")
    # column 0 of A, times 10 (A^-1 -> nm^-1)
    assert a_vals == pytest.approx([0.016 * 10, 0.0 * 10, 0.001 * 10], abs=1e-6)

    # only the three indexed peaks become reflections
    num_refl = next(line for line in crystal if line.startswith("num_reflections = "))
    assert int(num_refl.split("=")[1]) == 3

    # parse the hkl reflection rows back
    header_i = crystal.index(
        "   h    k    l          I   sigma(I)       peak background  "
        "fs/px  ss/px panel"
    )
    end_i = crystal.index("End of reflections")
    refl_rows = crystal[header_i + 1 : end_i]
    assert len(refl_rows) == 3
    parsed_hkl = {tuple(int(t) for t in row.split()[:3]) for row in refl_rows}
    assert parsed_hkl == {(1, 0, 0), (0, 1, -1), (2, -1, 3)}


def test_data_offloader_uses_lattice_metadata_from_cell(tmp_path, geometry_dict, cell):
    out = tmp_path / "indexed.stream"
    with DataOffloader(out, geometry=geometry_dict, cell=cell) as off:
        off.write(_make_index_result(cell))

    lines = out.read_text().splitlines()
    assert f"lattice_type = {cell.lattice_type}" in lines
    assert f"centering = {cell.centering}" in lines
    assert f"unique_axis = {cell.unique_axis}" in lines


def test_peak_offloader_writes_peaks_only_chunk(tmp_path, geometry_dict):
    out = tmp_path / "peaks.stream"
    with PeakOffloader(out, geometry=geometry_dict) as off:
        off.write(_make_peak_result())

    lines = out.read_text().splitlines()
    assert "----- Begin chunk -----" in lines
    assert "----- End chunk -----" in lines
    assert "indexed_by = none" in lines
    assert "num_peaks = 3" in lines
    assert "Peaks from peak search" in lines
    # peaks-only: no crystal block
    assert "--- Begin crystal" not in lines
    assert "num_reflections" not in "\n".join(lines)


