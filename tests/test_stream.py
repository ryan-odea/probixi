from probixi.peakfinding.peaks.peakfinder import PeakResult


def _make_index_result(cell) -> IndexResult:
    torch.manual_seed(0)
    # three indexed peaks, one un-indexed, so the crystal block lists only the kept ones
    positions = torch.tensor(
        [[40.0, 55.0], [70.0, 30.0], [90.0, 110.0], [10.0, 12.0]],
        dtype=torch.float64,
    )
    intensities = torch.tensor([1200.0, 800.0, 450.0, 50.0], dtype=torch.float64)
    sigmas = torch.tensor([35.0, 28.0, 21.0, 7.0], dtype=torch.float64)
    indexed_mask = torch.tensor([True, True, True, False])
    hkl = torch.tensor([[1, 0, 0], [0, 1, -1], [2, -1, 3], [0, 0, 0]], dtype=torch.long)
    # A = U @ B; use identity-ish reciprocal basis, distinct columns so a*/b*/c* differ
    A = torch.tensor(
        [[0.016, 0.001, 0.000], [0.000, 0.016, 0.002], [0.001, 0.000, 0.009]],
        dtype=torch.float64,
    )
    U = torch.eye(3, dtype=torch.float64)
    B = A.clone()
    return IndexResult(
        frame_index=0,
        n_peaks=4,
        n_indexed=3,
        rmsd=0.0042,
        A=A,
        U=U,
        B=B,
        cell=cell,
        indexed_mask=indexed_mask,
        hkl=hkl,
        positions=positions,
        intensities=intensities,
        sigmas=sigmas,
        loss_history=torch.zeros(1, dtype=torch.float64),
        scale=None,
    )


def _make_peak_result() -> PeakResult:
    rows = torch.tensor([40.0, 70.0, 90.0], dtype=torch.float64)
    cols = torch.tensor([55.0, 30.0, 110.0], dtype=torch.float64)
    isum = torch.tensor([1200.0, 800.0, 450.0], dtype=torch.float64)
    n = rows.numel()
    el = torch.arange(1, n + 1, dtype=torch.long)
    z = torch.zeros(n, dtype=torch.float64)
    stats = BlobStats(
        label_id=el,
        size=torch.full((n,), 9, dtype=torch.long),
        row_centroid=rows,
        col_centroid=cols,
        bbox_r0=el,
        bbox_r1=el,
        bbox_c0=el,
        bbox_c1=el,
        intensity_sum=isum,
        intensity_sigma=z,
        intensity_max=z,
        z_max=z,
        log_bf_sum=z,
        posterior_mean=z,
        eccentricity=z,
        peakedness=z,
    )
    return PeakResult(
        frame_index=0,
        scores={},
        labels=torch.zeros(1, 1, dtype=torch.long),
        stats=stats,
        keep=torch.ones(n, dtype=torch.bool),
    )


def _section(lines: list[str], start: str, end: str) -> list[str]:
    i = lines.index(start)
    j = lines.index(end, i)
    return lines[i : j + 1]


def test_written_stream_parses_back(tmp_path, geometry_dict, cell, geom_file):
    out = tmp_path / "indexed.stream"
    with DataOffloader(
        out, geometry=geometry_dict, cell=cell, geometry_file=geom_file
    ) as off:
        off.write(_make_index_result(cell))

    lines = out.read_text().splitlines()
    # header: version line + provenance
    assert lines[0] == "CrystFEL stream format 2.3"
    assert any(line == "Generated by probixi" for line in lines)
    # one chunk, framed by begin/end markers
    assert "----- Begin chunk -----" in lines
    assert "----- End chunk -----" in lines
    assert lines.index("----- Begin chunk -----") < lines.index("----- End chunk -----")


